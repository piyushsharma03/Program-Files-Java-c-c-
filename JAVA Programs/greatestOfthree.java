public class greatestOfthree {

    public static void main(String arg[]) {

        int a = 10;
        int b = 2, c = 40;

        if (a > b && a > c) {
            System.out.println(" a is greater among three ");
        } else if (b > c) {
            System.out.println(" b is greater among three ");
        } else {
            System.out.println(" c is greater among three ");
        }
    }

}
